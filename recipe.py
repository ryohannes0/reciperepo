# 1. go to github 
# 2. create a new repository 
# 3. ensure you are in the codespaces tab and click the plus button next to codespaces, this will redirect you to the website version of VS Code 
# 4. In the website, click the 3 horizontal lines on the top left corner, click this and navigate to the “Open in VS Code Desktop” option
# 5. Once you are on the desktop version, on the home page there is a “connect to” option 
# 6. Click connect to and this will open the search bar where there is a “Connect to Codespace” option
# 7. After clicking this, the searchbar will prompt you to click the codespace you created through Github, click it. 
# 8. open a terminal within the repository and create a veirtual enviornment by typing “python3 -m venv “name of your preferred virual environment”” 
# 9. activate virtual environment by typing “source “name of virtual environment here”/bin/activate”
# 10. create a new file through the terminal command “nano ‘name of file’.py”
# 11. open the sidebar menu on the left side and click the symbol that contain four squares
# 12. find the drop down menu subtab called “Codespaces” 
# 13. There should be a cloud symbol next to the subtab with an arrow, click this 
# 14. It will bring up the search bar displaying extensions to install
# 15. install all extensions with “Python” and “Jupyter” and click the “OK” button (blue) this makes the .py file interactive
# 16. add contents to the .py file
# 17. now push to your repository by following these steps: first trype into your terminal “git add . “ then “git commit -m ‘personalized message’” lastly “git push origin main” 

